
drop table IF EXISTS contacts;
create table contacts (
contact_id bigint generated by default as identity
, type varchar(255)
, email varchar(255)
, phone varchar(255)
, primary key (contact_id));


drop table IF EXISTS addresses;
create table addresses (
address_id bigint generated by default as identity
, street varchar(255)
, state varchar(255)
, country varchar(255)
, primary key (address_id));


drop table IF EXISTS departments;
create table departments (
department_id bigint generated by default as identity
, name varchar(255)
, primary key (department_id));

drop table IF EXISTS projects;
create table projects (
project_id bigint generated by default as identity
, name varchar(255)
, startDate DATE
, endDate DATE
, primary key (project_id));


drop table IF EXISTS employees;
create table employees (
employee_id bigint generated by default as identity
, career varchar(255)
, first_name varchar(255)
, last_name varchar(255)
, primary key (employee_id));


drop table IF EXISTS departments_addresses;
create table departments_addresses (
   department_id bigint not null
, address_id bigint not null);
alter table departments_addresses add constraint FK_address_id_addresses_departments_addresses foreign key (address_id) references addresses;
alter table departments_addresses add constraint FK_department_id_departments_departments_addresses foreign key (department_id) references departments;


drop table IF EXISTS departments_contacts;
create table departments_contacts (
department_id bigint not null
, contact_id bigint not null);
alter table departments_contacts add constraint FK_contact_id_contacts_departments_contacts foreign key (contact_id) references contacts;
alter table departments_contacts add constraint FK_department_id_departments_departments_contact foreign key (department_id) references departments;


